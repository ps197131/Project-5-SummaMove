namespace SummaMoveWPF.Models
{
    public class Users
    {
        private int id;

        public int ID
        {
            get { return id; }
            set { id = value; }
        }

        private string name;
        public string Name
        {
            get { return name; }
            set { name = value; }
        }

        private string email;
        public string Email
        {
            get { return email; }
            set { email = value; }
        }
        //hier hebben we een class aangemaakt met de eigenschappen van de users 


    }
}
